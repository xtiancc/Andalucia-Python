print("Calculadora Python")

# Variables
print("Inserte primer número: ")
num1 = int(input())
print("Inserte segundo número: ")
num2 = int(input())

# Calculos
suma = num1 + num2
resta = num1 - num2
multiplicación = num1 * num2
division = num1 / num2
cociente = num1 // num2
resto = num1 % num2

# Pantalla
print("- - - Resultados - - -")
print("Suma:" + str(suma))
print("Resta:", resta)
print("Multiplicación:", multiplicación)
print("División:", division)
print("Cociente:", cociente)
print("Resto:", resto)